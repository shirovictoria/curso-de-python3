# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e antecessor.
número_digitado = int(input('Digite um número para mostrar seu sucessor e antecessor: '))
print(f'Você digitou o número: {número_digitado}! Seu antecessor é {número_digitado - 1} e seu sucessor é {número_digitado + 1}.')
