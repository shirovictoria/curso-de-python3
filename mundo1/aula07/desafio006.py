# Crie um programa que leia um número e mostre o seu dobro. triplo e raiz quadrada.
número_digitado = int(input('Digite um número para mostrar seu dobro, triplo e a raiz quadrada: '))
print(f'Você digitou o número: {número_digitado}. O seu dobro é {número_digitado * 2}, o seu triplo é {número_digitado * 3} e a sua raiz quadrada é {número_digitado ** (1/2)}.')
